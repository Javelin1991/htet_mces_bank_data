function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)



%diary on
%diary('output.txt')

global abc def ru rmse_gui mse_gui r_gui no_in no_out runningmode

runningmode = str2num(get(handles.edit20,'String'));   % if mode is 0 ->usual prediction system, if it is 1 -> prediction providing train and test data separately, is for Benchmarking

data = load(get(handles.edit1,'String'));

epochs = str2num(get(handles.edit21,'String'));
alpha = str2num(get(handles.edit6,'String'));
beta = str2num(get(handles.edit7,'String'));
eta = str2num(get(handles.edit8,'String'));
ff = str2num(get(handles.edit17,'String'));  % forgetting factor

numSamples = [];

IND = str2num(get(handles.edit13,'String'));
OUTD = str2num(get(handles.edit14,'String'));
n = str2num(get(handles.edit15,'String'));  % cross validation parameter

%data will store ALL the data



%--------------making dataaraay------------------------------------------

dataarray = cell(n,1); % will change this 3 to n later

newmm = 1;

divisibleSize = size(data,1);


while mod(divisibleSize,n) ~= 0   % hv to change 3 to n
    divisibleSize = divisibleSize - 1;
    
end

for nn = 1:n

    temp = [];
    
    ii=1;
    
for mm = newmm: (newmm +((divisibleSize/n) - 1)) % hv to change 3 to n
    
    temp(ii,:) = data(mm,:);
    ii = ii + 1;
end

newmm = mm+1;

dataarray{nn,1} = temp;

end

%dataarray will store all the 3 parts of the data stored as individual
%matrices


%---------putting the data under Training data and Testing data----------

if runningmode == 0

if n~=1     % special case for n equal to 1

for pp=1:n   
    
    TrainData = [];
    test = [];
    
    test = dataarray{pp,1};   % we put 1 part of the data under testdata
    
    for qq=1:n
        
        if (qq ~= pp)    % all data other than the test data is put under training data

    TrainData = [TrainData; dataarray{qq,1}];
        end
        
    end
    
    str1 = sprintf('---------------- Cross Validation %d ------------------', pp);
    disp (str1);
    
    if isempty(numSamples)
predicted = Run_SaFIN(TrainData,test,IND,OUTD,alpha,beta,epochs,eta,ff);
    else
predicted = Run_SaFIN(TrainData,test,IND,OUTD,alpha,beta,epochs,eta,ff,numSamples);
    end

end

else
    test = dataarray{1,1};
    TrainData = dataarray{1,1};
    
    if isempty(numSamples)
predicted = Run_SaFIN(TrainData,test,IND,OUTD,alpha,beta,epochs,eta,ff);
    else
predicted = Run_SaFIN(TrainData,test,IND,OUTD,alpha,beta,epochs,eta,ff,numSamples);
    end
    
end


elseif runningmode == 1
    
  
    train = load(get(handles.edit18,'String'));
  
    test = load(get(handles.edit19,'String'));
   
   
    if isempty(numSamples)
predicted = Run_SaFIN(train,test,IND,OUTD,alpha,beta,epochs,eta,ff);
    else
predicted = Run_SaFIN(train,test,IND,OUTD,alpha,beta,epochs,eta,ff,numSamples);
    end
    

end

numRules = size(ru, 1);

%numInpCluster = sum(no_InTerms);
numInpCluster = sum(abc);

%numOutCluster = sum(no_OutTerms);
numOutCluster = sum(def);


set(handles.text10,'String',numRules);
set(handles.text11,'String',numInpCluster);
set(handles.text12,'String',numOutCluster);

set(handles.text15,'String',mse_gui);
%set(handles.text15,'String',MSE);

set(handles.text31,'String',rmse_gui);
%set(handles.text31,'String',RMSE);

set(handles.text17,'String',r_gui);
%set(handles.text17,'String',R);

set(handles.text21,'String',no_in);
set(handles.text22,'String',no_out);





diary off



